function getQuoteForDate(dateStr) {
  let hash = 0;
  for (let i = 0; i < dateStr.length; i++) {
    hash = (hash * 31 + dateStr.charCodeAt(i)) | 0;
  }
  const index = Math.abs(hash) % QUOTES.length;
  return QUOTES[index];
}

export default function DailyQuote({ selectedDate }) {
  const quote = getQuoteForDate(selectedDate);

  return (
    <div className="quote-banner">
      <span className="quote-banner-moon">🌙</span>
      <div className="quote-banner-content">
        <div className="quote-banner-text">{quote.text}</div>
        <div className="quote-banner-ref">{quote.ref}</div>
      </div>
    </div>
  );
}
